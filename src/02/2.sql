insert into rps_plays
  with plays_concat as (
  select rownum id
       , column_value play
    from table( select apex_string.split(input) from rps_input)
)
select id
     , translate(regexp_substr(play, '\S', 1, 1), 'ABC', 'RPS') as shape_opo
     , translate(regexp_substr(play, '\S', 1, 2), 'XYZ', 'RPS') as shape_own
     , regexp_substr(play, '\S', 1, 2) as encrypted_own
  from plays_concat
;

prompt First 10 play results
with data as (
select id
     , shape_opo
     , shape_own
     , case when encrypted_own = 'Z' then 6
            when encrypted_own = 'X' then 0
            else 3
       end 
       as result_points
     , case when encrypted_own = 'Z' then loses_against
            when encrypted_own = 'X' then wins_against
            else shape_opo
       end 
       as what_i_need_to_play
     , encrypted_own
  from rps_plays p
  join rps_options o
    on p.shape_opo = o.shape
)
   select id
       , shape_opo
       , encrypted_own
       , what_i_need_to_play
       , o.points + result_points as points 
    from data d
    join rps_options o
      on d.what_i_need_to_play = o.shape
  order by id
  fetch first 10 rows only;

prompt Score
with data as (
select id
     , shape_opo
     , shape_own
     , case when encrypted_own = 'Z' then 6
            when encrypted_own = 'X' then 0
            else 3
       end 
       as result_points
     , case when encrypted_own = 'Z' then loses_against
            when encrypted_own = 'X' then wins_against
            else shape_opo
       end 
       as what_i_need_to_play
  from rps_plays p
  join rps_options o
    on p.shape_opo = o.shape
), own_play_points as (
  select id
       , shape_opo
       , what_i_need_to_play
       , o.points + result_points as points 
    from data d
    join rps_options o
      on d.what_i_need_to_play = o.shape
)
select sum(points) as score
  from own_play_points
;

